package Model;

public class Lesson {
	private String lesson_id;
	private String lesson_name;
	private String lesson_password;
	private String lesson_day;


	public String getLesson_id() {
		return lesson_id;
	}

	public void setLesson_id(String lesson_id) {
		this.lesson_id = lesson_id;
	}

	public String getLesson_name() {
		return lesson_name;
	}

	public void setLesson_name(String lesson_name) {
		this.lesson_name = lesson_name;
	}

	public String getLesson_password() {
		return lesson_password;
	}

	public void setLesson_password(String lesson_password) {
		this.lesson_password = lesson_password;
	}

	public String getLesson_day() {
		return lesson_day;
	}

	public void setLesson_day(String lesson_day) {
		this.lesson_day = lesson_day;
	}

}
