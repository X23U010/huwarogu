package Model;

public class Public_Absence {
	
	public String public_absence_id;
	public String student_id;
	public String application_date;
	public String activity_date;
	public String activity_end_date;
	public String start_time;
	public String end_time;
	public String company_name;
	public String location;
	public String reason;
	public String selection_details;
	public boolean review_status;
	public String submission_status;
	
	


	public String getPublic_absence_id() {
		return public_absence_id;
	}

	public void setPublic_absence_id(String public_absence_id) {
		this.public_absence_id = public_absence_id;
	}

	public String getStudent_id() {
		return student_id;
	}

	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}

	public String getApplication_date() {
		return application_date;
	}

	public void setApplication_date(String application_date) {
		this.application_date = application_date;
	}

	public String getActivity_date() {
		return activity_date;
	}

	public void setActivity_date(String activity_date) {
		this.activity_date = activity_date;
	}

	public String getActivity_end_date() {
		return activity_end_date;
	}

	public void setActivity_end_date(String activity_end_date) {
		this.activity_end_date = activity_end_date;
	}

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public String getEnd_time() {
		return end_time;
	}

	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}

	public String getCompany_name() {
		return company_name;
	}

	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getSelection_details() {
		return selection_details;
	}

	public void setSelection_details(String selection_details) {
		this.selection_details = selection_details;
	}

	public boolean getReview_status() {
		return review_status;
	}

	public void setReview_status(boolean review_status) {
		this.review_status = review_status;
	}

	public String getSubmission_status() {
		return submission_status;
	}

	public void setSubmission_status(String submission_status) {
		this.submission_status = submission_status;
	}

}
